// looping
// Print the numbers from 1 to 5 to the console using a loop 

console.log("Using a for loop")

for(let i=1;i<=5;i++){
    console.log(i);
}
// or

console.log("Using a while loop")

let i = 1;
while(i<=5){
    console.log(i)
    i++;
}

// or
console.log("Using a do-while loop")
let j = 1;
do{
    console.log(j);
    j++;
}while(j<=5)